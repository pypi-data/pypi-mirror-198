class Authoritarianism:
    """
Explanation of Characteristics of Authoritarianism:

Concentration of Power: In an authoritarian system, power is concentrated in the hands of a single person or group, often a dictator or ruling party.
Lack of Accountability: Authoritarian leaders are not held accountable for their actions and do not face consequences for abusing their power.
Limited Political Participation: Authoritarian regimes often restrict or prevent citizens from participating in the political process, including limiting or controlling elections, freedom of speech, and freedom of the press.
Suppression of Opposition: Authoritarian leaders often suppress or eliminate opposition groups, including political parties, civil society organizations, and the media.
Use of Force: Authoritarian regimes often use force and intimidation to maintain their power, including the use of secret police, military force, and state-sponsored violence.    
    """
    def __init__(self, leader, ruling_party, secret_police):
        self.leader = leader
        self.ruling_party = ruling_party
        self.secret_police = secret_police
        self.power_concentration = True
        self.limited_participation = True
        self.opposition_suppression = True
        self.use_of_force = True
        self.accountability = False
